from __future__ import annotations

import sys
from collections.abc import Callable, Collection, Mapping, Sequence
from types import GenericAlias
from typing import (
    _SpecialForm,
    Annotated,
    Any,
    ClassVar,
    Iterator,
    Literal,
    NoReturn,
    Optional,
    Tuple,
    Type,
    TypeVar,
    Union,
    # This is required for old annotations like Dict[str, int], List[int],
    # etc. Newer python versions use dict[str, int] or list[int].
    _GenericAlias,  # type: ignore
    get_args,
    get_origin
)

from pytypeinfo.typing import NoneType, UnionType, get_type_hints


# -----------------------------------------------------------------------------
# Constants
# -----------------------------------------------------------------------------

_IMMUTABLE_TYPES: set[type] = {
    Collection,
    Mapping,
    Sequence,
    tuple
}

_META_ATTR = '__metadata__'
_BOUND_ATTR = '__bound__'
_CONSTRAINTS_ATTR = '__constraints__'

# -----------------------------------------------------------------------------
# Types
# -----------------------------------------------------------------------------

_HINT_TYPE = Any

_ARG_TYPES = (type, _GenericAlias, GenericAlias, _SpecialForm)

# -----------------------------------------------------------------------------
# Functions
# -----------------------------------------------------------------------------

if sys.version_info < (3, 9):
    def _is_annotation(hint: Any) -> bool:  # noqa: ANN401
        meta = getattr(hint, '__metadata__', None)
        return isinstance(meta, Sequence) and len(meta) > 0

else:
    def _is_annotation(hint: Any) -> bool:  # noqa: ANN401
        return get_origin(hint) is Annotated


def _resolve_sub_types(hint: _HINT_TYPE) -> Tuple[TypeInfo, ...]:
    sub_types = []
    for arg in get_args(hint):
        if isinstance(arg, _ARG_TYPES) or arg is Ellipsis:
            sub_types.append(TypeInfo(arg))
    return tuple(sub_types)


def _raise_type_error(
    required_type: Type[Any],
    obj: Any            # noqa: ANN401
) -> NoReturn:
    msg = (
        f'Invalid type: object must be {required_type.__name__!r} but'
        f' is {type(obj).__name__!r}'
    )
    raise TypeError(msg)


def _raise_member_error(
    required_member: str,
    obj: Any            # noqa: ANN401
) -> NoReturn:
    msg = (
        f'Object of type {type(obj).__name__!r} is missing member '
        f'{required_member!r}'
    )
    raise KeyError(msg)


def _raise_length_error(
    expect_len: int,
    actual_len: int,
    name: str
) -> NoReturn:
    name = (' ' + name).strip()
    msg = (
        f'Invalid length:{name} length is {actual_len} '
        f'but must be {expect_len}'
    )
    raise ValueError(msg)


def _raise_class_error(
    required_class: Optional[Type],
) -> NoReturn:
    if required_class is None:
        msg = 'Object must be a class'
    else:
        msg = (
            f'Object must be class {required_class.__name__!r} '
            'or subclass thereof'
        )
    raise TypeError(msg)


